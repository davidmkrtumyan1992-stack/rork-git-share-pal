// ─── Projection ──────────────────────────────────────────────────────────────
function project(lat: number, lng: number, rotDeg: number) {
  const phi   = (90 - lat) * (Math.PI / 180);
  const theta = (lng + rotDeg) * (Math.PI / 180);
  return {
    x: CX + R * Math.sin(phi) * Math.cos(theta),
    y: CY - R * Math.cos(phi),
    z: Math.sin(phi) * Math.sin(theta), // > 0 → front hemisphere
  };
}

function buildContinentPath(poly: Poly, rotDeg: number): string {
  let d   = '';
  let pen = false;
  for (const [lat, lng] of poly) {
    const { x, y, z } = project(lat, lng, rotDeg);
    if (z > 0) {
      d += pen
        ? `L${x.toFixed(1)},${y.toFixed(1)}`
        : `M${x.toFixed(1)},${y.toFixed(1)}`;
      pen = true;
    } else {
      if (pen) d += 'Z';
      pen = false;
    }
  }
  if (pen) d += 'Z';
  return d;
}

function buildGrid(rotDeg: number): string {
  let d = '';
  for (const lat of [-60, -30, 0, 30, 60]) {
    let mv = true;
    for (let lng = -180; lng <= 180; lng += 4) {
      const { x, y, z } = project(lat, lng, rotDeg);
      if (z > 0) { d += mv ? `M${x.toFixed(1)},${y.toFixed(1)}` : `L${x.toFixed(1)},${y.toFixed(1)}`; mv = false; }
      else mv = true;
    }
  }
  for (let lng = -150; lng < 180; lng += 30) {
    let mv = true;
    for (let lat = -85; lat <= 85; lat += 3) {
      const { x, y, z } = project(lat, lng, rotDeg);
      if (z > 0) { d += mv ? `M${x.toFixed(1)},${y.toFixed(1)}` : `L${x.toFixed(1)},${y.toFixed(1)}`; mv = false; }
      else mv = true;
    }
  }
  return d;
}

// ─── Component ───────────────────────────────────────────────────────────────
export function CommunityGlobe() {
  const [frame, setFrame] = useState(0);

  useEffect(() => {
    const id = setInterval(() => setFrame(f => f + 1), 50);
    return () => clearInterval(id);
  }, []);

  const rotDeg = frame * 0.3;
  const continentPaths = CONTINENTS.map(p => buildContinentPath(p, rotDeg));
  const grid = buildGrid(rotDeg);

  const labels = LABELS.map(l => ({ ...l, ...project(l.lat, l.lng, rotDeg) }))
    .filter(l => l.z > 0.15);

  return (
    <View style={styles.wrapper}>
      <Svg width={SIZE} height={SIZE} viewBox={`0 0 ${SIZE} ${SIZE}`}>
        <Defs>
          {/* Globe base — bright top-left → darker edge */}
          <RadialGradient id="gBase" cx="36%" cy="30%" r="72%">
            <Stop offset="0%"   stopColor="#FFFFFF" />
            <Stop offset="50%"  stopColor="#F0EFEB" />
            <Stop offset="100%" stopColor="#BFBDB7" />
          </RadialGradient>
          {/* Atmosphere halo */}
          <RadialGradient id="gAtm" cx="50%" cy="50%" r="50%">
            <Stop offset="72%"  stopColor="#A8B0B8" stopOpacity="0"    />
            <Stop offset="86%"  stopColor="#A8B0B8" stopOpacity="0.12" />
            <Stop offset="100%" stopColor="#A8B0B8" stopOpacity="0"    />
          </RadialGradient>
          {/* Specular highlight — bright spot top-left */}
          <RadialGradient id="gSpec" cx="30%" cy="26%" r="38%">
            <Stop offset="0%"   stopColor="#FFFFFF" stopOpacity="0.7" />
            <Stop offset="100%" stopColor="#FFFFFF" stopOpacity="0"   />
          </RadialGradient>
          {/* Edge shadow — gives sphere depth */}
          <RadialGradient id="gEdge" cx="50%" cy="50%" r="50%">
            <Stop offset="68%"  stopColor="#000000" stopOpacity="0"    />
            <Stop offset="100%" stopColor="#000000" stopOpacity="0.25" />
          </RadialGradient>
          <ClipPath id="gClip">
            <Circle cx={CX} cy={CY} r={R} />
          </ClipPath>
        </Defs>

        {/* Atmosphere ring */}
        <Circle cx={CX} cy={CY} r={R + 16} fill="url(#gAtm)" />

        {/* Globe base */}
        <Circle cx={CX} cy={CY} r={R} fill="url(#gBase)" />

        <G clipPath="url(#gClip)">
          {/* Latitude / Longitude grid */}
          <Path d={grid} stroke="#CCCBC5" strokeWidth="0.3" strokeOpacity="0.4" fill="none" />

          {/* Continent fills */}
          {continentPaths.map((d, i) =>
            d ? (
              <Path
                key={i}
                d={d}
                fill="#ABABА5"
                fillOpacity="0.95"
                stroke="#9A9892"
                strokeWidth="0.45"
                strokeOpacity="1"
              />
            ) : null
          )}

          {/* Country labels */}
          {labels.map((l, i) => {
            const op = Math.min(1, (l.z - 0.15) * 4);
            return (
              <SvgText
                key={i}
                x={l.x}
                y={l.y}
                fontSize={l.size}
                fill="#4A4845"
                fillOpacity={op}
                fontWeight="600"
                textAnchor="middle"
                letterSpacing="0.5"
              >
                {l.name}
              </SvgText>
            );
          })}
        </G>

        {/* Edge darkening — 3-D depth */}
        <Circle cx={CX} cy={CY} r={R} fill="url(#gEdge)" clipPath="url(#gClip)" />

        {/* Specular highlight */}
        <Circle cx={CX} cy={CY} r={R} fill="url(#gSpec)" clipPath="url(#gClip)" />

        {/* Outer border */}
        <Circle cx={CX} cy={CY} r={R} fill="none" stroke="#BCBAB4" strokeWidth="1" />
      </Svg>
    </View>
  );
}

